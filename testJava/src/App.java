public class App {
    public static void main(String[] args) throws Exception {
        suma(10, 30);

        System.out.println();
    }

    public static void suma(int a, int b){
        System.out.println(a + b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a - b);
        System.out.println(a % b);

    }
}
